            <table align="center" width="90%" border="5" cellpadding="10" cellspacing="5" style="color:red; border-color:black">
                <h1 align="center"><u>Difference Between Class And Interface</u></h1> 
                <tr>
                    <th><b style="font-size:25px">Points</b></th>
                    <th><b style="font-size:25px">Class</b></th>
                    <th><b style="font-size:25px">Interface</b></th>
                </tr>
                
                <tr>
                    <td>Definition</td>
                    <td>A class describes the attributes and behaviors of an object.</td>
                    <td>Writing an interface is similar to writing a class. An interface contains behaviors that a class implements.</td>
                </tr>

                <tr>
                    <td>Multiple Inheritance</td>
                    <td>Class does not support multiple inheritance.</td>
                    <td>Interface support multiple inheritance.</td>
                </tr>

                <tr>
                    <td>Data-Member</td>
                    <td>Class contains data members.</td>
                    <td>Interface does not contains data members.</td>
                </tr>

                <tr>
                    <td>Constructors</td>
                    <td>Class contains constructors.</td>
                    <td>Interface does not contains constructors.</td>
                </tr>

                <tr>
                    <td>Static Members</td>
                    <td>Only complete members of class can be Static.</td>
                    <td>Members of interface can not be Static.</td>
                </tr>

                <tr>
                    <td>Inherit Class</td>
                    <td>A class can inherit another class.</td>
                    <td>An Interface cannot inherit a class.</td>
                </tr>

                <tr>
                    <td>Keywords</td>
                    <td>A class can be inherited by another class using the keyword 'extends'.</td>
                    <td>An Interface can be inherited by a class using the keyword 'implements' and it can be inherited by another interface using the keyword 'extends'.</td>
                </tr>

                <tr>
                    <td>Existance Of Body</td>
                    <td>A method body can exist in a class.</td>
                    <td>A method body cannot exist in an interface.</td>
                </tr>
            </table>
